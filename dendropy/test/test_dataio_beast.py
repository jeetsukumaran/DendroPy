        #    print("    },")

    def testSummaryTreeIter(self):
        stream = pathmap.tree_source_stream('pythonidae.beast.summary.tre')
        for tree in dendropy.tree_source_iter(stream, 'beast-summary-tree'):
            self.verify_node_info(tree)

